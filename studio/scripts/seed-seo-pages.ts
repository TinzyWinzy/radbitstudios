async function seed() {
  console.log("Seeding SEO pages...");

  let created = 0;
  let skipped = 0;
  for (const page of pages) {
    const existing = await collection
      .where("slug", "==", page.slug)
      .where("type", "==", page.type)
      .get();

    if (!existing.empty) {
      console.log(`  Skipping "${page.slug}" (${page.type}) — already exists`);
      skipped++;
      continue;
    }

    const docData: Record<string, unknown> = {
      ...page,
      published: true,
      createdAt: FieldValue.serverTimestamp(),
      updatedAt: FieldValue.serverTimestamp(),
    };

    await collection.add(docData);
    console.log(`  Created "${page.slug}" (${page.type})`);
    created++;
  }

  console.log(`\nDone. Created ${created} new pages, skipped ${skipped}.`);
  process.exit(0);
}

seed().catch((err) => {
  console.error("Seed failed:", err);
  process.exit(1);
});
